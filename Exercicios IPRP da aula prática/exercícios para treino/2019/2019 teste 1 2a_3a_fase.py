#as caracteristicas fundamentais são:
a = 10
b = 'ola teste'
c = 0.5

#a IDENTIDADE do objeto, onde ele esta guardado na memória, a sua identidade unica
print(f"{id(a)}, {id(b)}, {id(c)}")

#o tipo do objeto, se é um numero inteiro, uma caracter, um float, um booleano.
print(f"{type(a)}, {type(b)}, {type(c)}")

#o valor nele guardado: 2, 'c', "ola", 0.5, 2i e o respetivo nome
print(f"{(a)}, {(b)}, {(c)}")
